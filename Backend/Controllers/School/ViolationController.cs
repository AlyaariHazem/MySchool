    [HttpDelete("{id:int}")]
    public async Task<ActionResult<APIResponse>> Delete(int id, CancellationToken cancellationToken)
    {
        var response = new APIResponse();
        try
        {
            var gate = await EnsureManagerMayAccessViolationSchoolAsync(id, cancellationToken);
            if (gate != null) return gate;

            var ok = await _unitOfWork.Violations.DeleteAsync(id, cancellationToken);
            if (!ok)
            {
                response.IsSuccess = false;
                response.statusCode = HttpStatusCode.NotFound;
                response.ErrorMasseges.Add("Violation was not found.");
                return NotFound(response);
            }

            response.Result = true;
